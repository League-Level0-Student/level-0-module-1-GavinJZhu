package _05_for_loops._2_badgers;

public class Badger {
    public static void main(String[] args) {
        for (int i = 0; i < 2; i++) {
            for (int j = 0; j < 12; j++) {
                System.out.println("Badger");
            }
            for (int l = 0; l < 2; l++) {
                System.out.println("Mushroom");
            }
        }
        System.out.println("A SNAKE!!!");
    }
}